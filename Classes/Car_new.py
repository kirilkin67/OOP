
# Класс Car
class Car:
    # Конструктор класса Car
    def __init__(self, license_plate, color, engine_volume, power):
        # Инициализация атрибутов экземпляра Car
        self.license_plate = license_plate  # Номерной знак автомобиля
        self.color = color  # Цвет автомобиля
        self.engine_volume = engine_volume  # Объем двигателя
        self.power = power  # Мощность двигателя
